function set(id, valor) {
  const el = document.getElementById(id);
  if (!el) return;
  if (valor.includes("\n")) {
    el.innerHTML = valor
      .split("\n\n")
      .map(p => "<p>" + p.replace(/\n/g, "<br>") + "</p>")
      .join("");
  } else {
    el.textContent = valor;
  }
}

function setLink(id, href, texto) {
  const el = document.getElementById(id);
  if (!el) return;
  el.href = href;
  el.textContent = texto;
}

